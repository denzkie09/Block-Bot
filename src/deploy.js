const { REST, Routes } = require('discord.js');
const { DISCORD_TOKEN, DISCORD_CLIENT_ID } = require('./config');
const { commands } = require('./commands/definitions');

const rest = new REST().setToken(DISCORD_TOKEN);

(async () => {
  try {
    console.log(`üì§ Deploying ${commands.length} slash command(s)...`);

    const data = await rest.put(
      Routes.applicationCommands(DISCORD_CLIENT_ID),
      { body: commands }
    );

    console.log(`‚úÖ Successfully registered ${data.length} command(s):`);
    data.forEach((cmd) => console.log(`   ‚Ä¢ /${cmd.name}`));
  } catch (error) {
    console.error('‚ùå Failed to deploy commands:', error);
  }
})();
